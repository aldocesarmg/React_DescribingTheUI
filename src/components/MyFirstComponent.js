// 1           2        3
export default function MyFirstComponent() {
    // 4   5
    return <h1>Hello world</h1>;
}

/*
1. export default: This will be the only function that this file will export (default export).
2. function: Components in React are functions.
3. MyFirstComponent(): Name of the component
4. return: All components need to return something (the UI piece which will be exported).
5. <h1>Hello world</h1>: The JSX code that defines the UI to be shown.
*/